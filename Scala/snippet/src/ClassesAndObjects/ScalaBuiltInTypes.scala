package ClassesAndObjects

object ScalaBuiltInTypes {
  // Numbers
  // Int = 42
  // Ints are 32-bit integers
  42

  // Double = 42.0
  // Doubles are 64-bit integers
  42.0

  // Float = 42.0
  // Floats are 32-bit floating points.
  42.0f

  // Long = 42
  // Long for 64-bit integers.
  42L

  // Short = 5
  // Short is a 16-bit integer and can only be created via the toShort method
  5.toShort

  // Byte = 5
  // Byte's are 8-bit integers and can only be created via the toByte method
  5.toByte


  // Misc.
  // Char
  'a'

  // Null
  // Considered bad practice in Scala
  null

  // Unit: ()
  // Scala's void type
  ()
}
